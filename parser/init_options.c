void	init_opt(t_opt *ident)
{
	ident->r[0] = -1;
	ident->r[1] = -1;
	ident->f[0] = -1;
	ident->f[1] = -1;
	ident->f[2] = -1;
	ident->c[0] = -1;
	ident->c[1] = -1;
	ident->c[2] = -1;
}
